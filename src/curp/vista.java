    /**
     * Metodo que genera y muestra la curp en pantalla
     */
    public void generarCURP() {
        if (camposVacios()) {
            JOptionPane.showMessageDialog(null, "Hay Campos Vacios");
            return;
        }
        String CURP = VALIDAR.getNombreFormato(getNombre(), getAP(), getAM());
        System.out.println(CURP);
        CURP += VALIDAR.getFechaFormato(getJcbDia(), getMes(), getAnio());
        CURP += getSexo();
        CURP += VALIDAR.getEstadoFormato(getJcbEstado());
        CURP += "" + VALIDAR.getConsonante(getAP().substring(1));
        System.out.println(CURP);

        CURP += "" + VALIDAR.getConsonante(getAM().substring(1));
        System.out.println(CURP);

        CURP += "" + VALIDAR.getConsonante(getNombre().substring(1));
        System.out.println(CURP);
        inserccion(CURP);
        JOptionPane.showMessageDialog(null, "Tu curp es: " + CURP);
    }

