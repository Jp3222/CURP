    private String[] campos = {
        "curp", "nombre", "ap", "am", "dia", "mes", "anio", "sexo", "estado"
    };

    public void inserccion(String curp) {
        String usuario[] = {
            curp,
            getNombre(),
            getAP(),
            getAM(),
            getJcbDia(),
            getMes(),
            getAnio(),
            getSexo(),
            getJcbEstado()
        };
        cn.insert("usuarios", cn.getCampos(campos), cn.getDatos(usuario));
        limpiar();
    }

    public boolean camposVacios() {
        if (!esValido(jtfNombre.getText())) {
            return true;
        }
        if (!esValido(jtfAP.getText())) {
            return true;
        }

        if (!esValido(jtfAM.getText())) {
            return true;
        }
        if (jcbMes.getSelectedIndex() < 1) {
            return true;
        }
        if (jcbDia.getSelectedIndex() < 1) {
            return true;
        }
        if (!esValido(jtfAnio.getText())) {
            return true;
        }
        if (jcbSexo.getSelectedIndex() < 1) {
            return true;
        }
        if (jcbEstado.getSelectedIndex() < 1) {
            return true;
        }
        return false;
    }

    public void limpiar() {
        jtfNombre.setText("");
        jtfAP.setText("");
        jtfAM.setText("");
        jcbMes.setSelectedIndex(0);
        jcbDia.setSelectedIndex(0);
        jtfAnio.setText("");
        jcbSexo.setSelectedIndex(0);
        jcbEstado.setSelectedIndex(0);
        
    }

    public boolean esValido(String text) {
        return text != null && !text.isEmpty();
    }

